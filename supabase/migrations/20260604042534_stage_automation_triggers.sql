CREATE OR REPLACE FUNCTION render_stage_automation_task_text(
  template TEXT,
  lead_name TEXT,
  stage_name TEXT
)
RETURNS TEXT AS $$
BEGIN
  RETURN REPLACE(
    REPLACE(template, '{{lead_name}}', COALESCE(lead_name, '')),
    '{{stage_name}}',
    COALESCE(stage_name, '')
  );
END;
$$ LANGUAGE plpgsql IMMUTABLE;

CREATE OR REPLACE FUNCTION handle_lead_stage_automation_triggers()
RETURNS TRIGGER AS $$
DECLARE
  trigger_row stage_automation_triggers%ROWTYPE;
  assigned_employee_id BIGINT;
  current_employee_id BIGINT;
  new_stage_name TEXT;
  scheduled_at TIMESTAMP WITH TIME ZONE;
BEGIN
  IF OLD.stage_id IS NOT DISTINCT FROM NEW.stage_id THEN
    RETURN NEW;
  END IF;

  SELECT id
  INTO current_employee_id
  FROM xodimlar
  WHERE employee_id = auth.uid()::text
  LIMIT 1;

  SELECT name
  INTO new_stage_name
  FROM stages
  WHERE id = NEW.stage_id;

  FOR trigger_row IN
    SELECT *
    FROM stage_automation_triggers
    WHERE pipeline_id = NEW.pipeline_id
      AND stage_id = NEW.stage_id
      AND enabled = true
      AND event_type = 'lead_entered_stage'
      AND action_type = 'create_task'
    ORDER BY created_at ASC
  LOOP
    assigned_employee_id := CASE trigger_row.assignee_mode
      WHEN 'specific_employee' THEN trigger_row.assignee_employee_id
      WHEN 'current_user' THEN current_employee_id
      ELSE NEW.employee_id
    END;

    assigned_employee_id := COALESCE(
      assigned_employee_id,
      NEW.employee_id,
      current_employee_id
    );

    scheduled_at := NOW() + (trigger_row.delay_minutes * INTERVAL '1 minute');

    INSERT INTO tasks (
      text,
      status,
      lead_id,
      date,
      time,
      employee_id,
      task_type
    )
    VALUES (
      render_stage_automation_task_text(
        trigger_row.task_text,
        NEW.name,
        new_stage_name
      ),
      false,
      NEW.id,
      (scheduled_at AT TIME ZONE trigger_row.schedule_timezone)::DATE,
      (scheduled_at AT TIME ZONE trigger_row.schedule_timezone)::TIME(0),
      assigned_employee_id,
      trigger_row.task_type
    );
  END LOOP;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS leads_stage_automation_triggers ON leads;
CREATE TRIGGER leads_stage_automation_triggers
  AFTER UPDATE OF stage_id ON leads
  FOR EACH ROW
  WHEN (OLD.stage_id IS DISTINCT FROM NEW.stage_id)
  EXECUTE FUNCTION handle_lead_stage_automation_triggers();
