-- Stage automation triggers for bigint-based CRM tables.
-- Admins save trigger settings here; Postgres creates reminder tasks when a lead moves into a configured stage.

CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

